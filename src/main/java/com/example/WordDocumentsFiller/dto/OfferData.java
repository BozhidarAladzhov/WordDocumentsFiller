package com.example.WordDocumentsFiller.dto;

public class OfferData {

    public String email;
    public String date;
    public String fullName;
    public String vehicle;
    public String domesticTransport;
    public String pickUpAddress;
    public String portOfLoading;
    public String oceanFreight;
    public String portOfDelivery;
    public String inlandTransport;
    public String validate;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getVehicle() {
        return vehicle;
    }

    public void setVehicle(String vehicle) {
        this.vehicle = vehicle;
    }

    public String getDomesticTransport() {
        return domesticTransport;
    }

    public void setDomesticTransport(String domesticTransport) {
        this.domesticTransport = domesticTransport;
    }

    public String getPickUpAddress() {
        return pickUpAddress;
    }

    public void setPickUpAddress(String pickUpAddress) {
        this.pickUpAddress = pickUpAddress;
    }

    public String getPortOfLoading() {
        return portOfLoading;
    }

    public void setPortOfLoading(String portOfLoading) {
        this.portOfLoading = portOfLoading;
    }

    public String getOceanFreight() {
        return oceanFreight;
    }

    public void setOceanFreight(String oceanFreight) {
        this.oceanFreight = oceanFreight;
    }

    public String getPortOfDelivery() {
        return portOfDelivery;
    }

    public void setPortOfDelivery(String portOfDelivery) {
        this.portOfDelivery = portOfDelivery;
    }

    public String getInlandTransport() {
        return inlandTransport;
    }

    public void setInlandTransport(String inlandTransport) {
        this.inlandTransport = inlandTransport;
    }

    public String getValidate() {
        return validate;
    }

    public void setValidate(String validate) {
        this.validate = validate;
    }
}
